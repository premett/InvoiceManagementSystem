    public static boolean checkDigit(long idNumber, long phoneNumber) {
        int counterDigitForId = 0;
        int counterDigitForPhone = 0;
        while (idNumber > 0) {
            idNumber /= 10;
            counterDigitForId++;
            
        }
        
        while (phoneNumber > 0) {
            phoneNumber /= 10;
            counterDigitForPhone++;
            
        }
        if (counterDigitForId == 11 && counterDigitForPhone == 10) {
            System.out.println("ok");
            System.out.println(counterDigitForId + " , " + counterDigitForPhone);
            return true;
        } else {
            System.out.println("no");
            
            return false;
        }
        
    }
