    private void generalStatistics(String operation,String what,String where,javax.swing.JLabel who) {
        try {
            conn = DataBaseConnection.DBConnection.connectionDB();
            String sql = "SELECT "+operation+"("+what+")"+ "FROM "+ where+";";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(who==sumPayment) {
             who.setText(String.format("%,.2f", rs.getDouble(1)));
            
            }else {
            who.setText(String.valueOf(rs.getInt(1)));
            }
            
            
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hata mesajÄ±");
        }

    }

